         // Get the modal
         var modal = document.getElementById("myModal");

         // Get the buttons that open the modal
         var btns = document.getElementsByClassName("home__button"); // Ajuste se necessário

         // Get the <button> element that closes the modal
         var closeButton = document.querySelector(".close");

         // Get the hidden input field for the plan type
         var planTypeInput = document.getElementById("planType");

         // Define the fixed plan type
         var fixedPlanType = "social media - sem plano";

         // When the user clicks the button, open the modal and set the plan type
         for (let btn of btns) {
            btn.addEventListener('click', function() {
               // Set the hidden input field value to the fixed plan type
               planTypeInput.value = fixedPlanType;

               // Open the modal
               modal.style.display = "block";
               modal.setAttribute("aria-hidden", "false");
            });
         }

         // When the user clicks on <button> (x), close the modal
         closeButton.addEventListener('click', function() {
            modal.style.display = "none";
            modal.setAttribute("aria-hidden", "true");
         });

         // When the user clicks anywhere outside of the modal, close it
         window.addEventListener('click', function(event) {
            if (event.target == modal) {
               modal.style.display = "none";
               modal.setAttribute("aria-hidden", "true");
            }
         });
      </script>

